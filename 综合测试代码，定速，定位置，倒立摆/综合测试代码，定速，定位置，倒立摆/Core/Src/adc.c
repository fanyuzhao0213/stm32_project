/**
 * @brief  每个通道采样5次，求平均
 * @param  avg_buffer 结果输出缓冲区（uint16_t[4]）
 */
void Read_ADC1_MultiChannel_Average(uint16_t *avg_buffer)
{
    const int sample_count = 1;
    uint32_t sum[4] = {0};

    for (int sample = 0; sample < sample_count; sample++)
    {
        for (int i = 0; i < 4; i++)
        {
			HAL_ADC_Start(&hadc1);
            // 等待一个通道转换完成
            if (HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY) == HAL_OK)
            {
                sum[i] += HAL_ADC_GetValue(&hadc1); // 顺序读取当前 Rank 的值
            }
        }

        HAL_ADC_Stop(&hadc1);
    }

    for (int i = 0; i < 4; i++)
    {
        avg_buffer[i] = sum[i] / sample_count;
    }
}


void Read_All_ADC_Channels(uint16_t* adc_values)
{
    Read_ADC1_MultiChannel_Average(adc_values);
}


uint16_t Read_Angle_Value(void)
{
	uint16_t adc_values;
	HAL_ADC_Start(&hadc2);		//启动ADC单次转换
	HAL_ADC_PollForConversion(&hadc2, 50);
	adc_values = HAL_ADC_GetValue(&hadc2); // 读取当前 adc 的值
	return adc_values;
}
